#include <stdio.h>

char    *ft_rot42(char *str)
{
	int     i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 97 && str[i] <= 106) || (str[i] >= 65 && str[i] <= 74))
			str[i] = str[i] + 16;
		else if ((str[i] > 106 && str[i] < 123) || (str[i] > 74 && str[i] < 91))
			str[i] -= 10;
		i++;
	}
	return (str);
}
