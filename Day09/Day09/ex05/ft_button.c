int		ft_button(int i, int j, int k)
{
	if ((i > j && i < k) || (i < j && i > k))
		return (i);
	if ((k > j && k < i) || (k < j && k > i))
		return (i);
	if ((j > i && j < k) || (j < i && j > k))
		return (i);
	else
		return (0);
}
