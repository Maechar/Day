int		ft_iterative_factorial(int nb)
{
	int		a;
	int		i;

	a = 1;
	i = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		return (1);
	while (nb > 0)
	{
		a = a * i;
		nb--;
		i++;
	}
	return (a);
}
