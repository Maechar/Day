int		ft_atoi(char *str)
{
	int		i;
	int		j;

	i = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t' || *str == '\v')
		str++;
	if (*str == '-')
		j = 1;
	if (*str != '\0' && *str >= 48 && *str <= 57)
		str++;
	while (*str != '\0' && *str >= 48 && *str <= 57)
	{
		i = (i * 10) + (*str - 48);
		str++;
	}
	if (j == i)
		return (-i);
	else
		return (i);

}
